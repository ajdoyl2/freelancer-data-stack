# Cleanup
agent_clean() {
    echo -e "${CYAN}ðŸ§¹ Starting cleanup${NC}"
    
    # Python cache
    find . -type d -name "__pycache__" -exec rm -rf {} + 2>/dev/null || true
    find . -name "*.pyc" -delete 2>/dev/null || true
    echo -e "${GREEN}âœ… Python cache cleaned${NC}"
    
    # Poetry cache
    poetry cache clear --all pypi --quiet
    echo -e "${GREEN}âœ… Poetry cache cleaned${NC}"
    
    # Docker cleanup
    docker system prune -f --volumes
    echo -e "${GREEN}âœ… Docker cleanup completed${NC}"
    
    echo -e "${GREEN}ðŸŽ¯ Cleanup completed!${NC}"
}

